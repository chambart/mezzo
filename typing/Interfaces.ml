(** This module helps dealing with interfaces. *)

open Types

(* ---------------------------------------------------------------------------- *)

(* Interface-related functions. *)

let get_exports env =
  let assoc =
    fold env (fun acc point ({ names; _ }, _) ->
      let canonical_names = List.filter is_user names in
      List.map (fun x -> x, point) canonical_names :: acc
    ) [] 
  in
  List.flatten assoc
;;


